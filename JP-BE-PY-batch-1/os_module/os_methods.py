os.getcwd()  # absolute path

os.listdir()  # returns list of files/folder

os.path.join()  # returns path as string

os.path.abspath()  # returns absolute path as string

os.path.isfile() # returns bool True/False

os.path.isdir()  # returns bool True/False

os.rename(src, destination)  # rename file or move file from one destination to other

os.remove(src)  # delete file
