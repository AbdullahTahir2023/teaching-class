# Assignemnt Operator
# below are the examples of assignment operators
# =		
# += 
# -= 
# *= 
# /= 
# %= 
# //= 




x = 5 # value 5 is saved in the variale x
print(x) # 5

# how to increment the value of x by 1
x = x + 1 #
print(x) # 6

# another way of incrementing the value of variable
x += 1
print(x) # 7




# other operators works similarly
# subtraction
y = 10
print(y)  # 10
y -= 2  # 10 - 2
print(y)  # 8
y -= 5  # 8-5
print(y)  # 3


# multiplication
y = 2
print(y) # 2
y *= 5 # 2 * 5
print(y) # 10
y *= 2 # 10 * 2
print(y) # 20


# division
y = 10
print(y)  # 10
y /= 2  # 10/2
print(y)  # 5.0
y /= 5  # 5.0/5
print(y)  # 1.0


# floor division
y = 10
print(y)  # 10
y /= 2  # 10/2
print(y)  # 5
y /= 5  # 5/5
print(y)  # 1


