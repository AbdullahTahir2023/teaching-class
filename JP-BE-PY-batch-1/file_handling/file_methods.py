read() # read whole file at once and return content as string

readlines()  # read whole file at once and return content as list separated by new line

readline()  # read each row line by line - memory efficient

write() # write string data in the file

writelines()  # write string data in the file take list as an input

close() # close file to free memory

flush() # free cache memory

seek()  # set pointer position

tell()  # returns current pointer position
